    /// A generated item model. Wraps [`crate::meshing::ItemModelResult`].
    ///
    /// Holds `Option<...>` because [`ItemModelResult::add_to_pack`] moves the
    /// inner value into an [`ItemModelPackBuilder`] (PORTING rule 11); accessors
    /// return `AlreadyConsumed` afterwards.
    #[diplomat::opaque_mut]
    pub struct ItemModelResult(pub(crate) Option<crate::meshing::ItemModelResult>);

    impl ItemModelResult {
        /// Generate an item model from a schematic (old ABI: `schematic_to_item_model`).
        pub fn create(
            schematic: &Schematic,
            pack: &ResourcePack,
            config: &ItemModelConfig,
        ) -> Result<Box<ItemModelResult>, NucleationError> {
            schematic
                .0
                .to_item_model(&pack.0, &config.0)
                .map(|r| Box::new(ItemModelResult(Some(r))))
                .map_err(|_| NucleationError::Mesh)
        }

        /// The Minecraft item model JSON.
        pub fn model_json(&self, out: &mut DiplomatWrite) -> Result<(), NucleationError> {
            let inner = self.0.as_ref().ok_or(NucleationError::AlreadyConsumed)?;
            let _ = write!(out, "{}", inner.model_json);
            Ok(())
        }

        /// Number of elements (cuboids) in the generated model.
        pub fn element_count(&self) -> Result<u32, NucleationError> {
            let inner = self.0.as_ref().ok_or(NucleationError::AlreadyConsumed)?;
            Ok(inner.stats.element_count as u32)
        }

        /// Number of textures the generated model uses.
        pub fn texture_count(&self) -> Result<u32, NucleationError> {
            let inner = self.0.as_ref().ok_or(NucleationError::AlreadyConsumed)?;
            Ok(inner.stats.texture_count as u32)
        }

        /// Number of textured planes in the generated model.
        pub fn plane_count(&self) -> Result<u32, NucleationError> {
            let inner = self.0.as_ref().ok_or(NucleationError::AlreadyConsumed)?;
            Ok(inner.stats.plane_count as u32)
        }

        /// Source schematic dimensions in blocks.
        pub fn dimensions(&self) -> Result<Dimensions, NucleationError> {
            let inner = self.0.as_ref().ok_or(NucleationError::AlreadyConsumed)?;
            let (x, y, z) = inner.stats.dimensions;
            Ok(Dimensions { x, y, z })
        }

        /// The applied model scale (old ABI: `itemmodel_result_scale`).
        pub fn scale(&self) -> Result<ItemScale, NucleationError> {
            let inner = self.0.as_ref().ok_or(NucleationError::AlreadyConsumed)?;
            let (x, y, z) = inner.stats.scale;
            Ok(ItemScale { x, y, z })
        }

        /// A single-model resource pack ZIP, base64-encoded.
        pub fn to_resource_pack_zip_b64(
            &self,
            out: &mut DiplomatWrite,
        ) -> Result<(), NucleationError> {
            let inner = self.0.as_ref().ok_or(NucleationError::AlreadyConsumed)?;
            let data = inner
                .to_resource_pack_zip()
                .map_err(|_| NucleationError::Serialize)?;
            super::write_b64(&data, out);
            Ok(())
        }

        /// Move this result into a pack builder. Consumes the result: further
        /// accessor calls return `AlreadyConsumed`.
        pub fn add_to_pack(
            &mut self,
            builder: &ItemModelPackBuilder,
        ) -> Result<(), NucleationError> {
            let inner = self.0.take().ok_or(NucleationError::AlreadyConsumed)?;
            builder
                .0
                .try_borrow_mut()
                .map_err(|_| NucleationError::Lock)?
                .push(inner);
            Ok(())
        }
    }

    /// Accumulates [`ItemModelResult`]s to build a combined resource pack ZIP
    /// (old ABI: `itemmodel_build_resource_pack` took an array of pointers).
    #[diplomat::opaque]
    pub struct ItemModelPackBuilder(
        pub(crate) std::cell::RefCell<Vec<crate::meshing::ItemModelResult>>,
    );

    impl ItemModelPackBuilder {
        /// Create an empty pack builder.
        pub fn create() -> Box<ItemModelPackBuilder> {
            Box::new(ItemModelPackBuilder(std::cell::RefCell::new(Vec::new())))
        }

        /// Number of results added so far.
        pub fn len(&self) -> u32 {
            self.0.borrow().len() as u32
        }

        /// Build a resource pack ZIP from every added result, base64-encoded.
        pub fn build_zip_b64(&self, out: &mut DiplomatWrite) -> Result<(), NucleationError> {
            let items = self.0.try_borrow().map_err(|_| NucleationError::Lock)?;
            if items.is_empty() {
                return Err(NucleationError::InvalidArgument);
            }
            let refs: Vec<&crate::meshing::ItemModelResult> = items.iter().collect();
            let data = crate::meshing::build_resource_pack(&refs)
                .map_err(|_| NucleationError::Serialize)?;
            super::write_b64(&data, out);
            Ok(())
        }
    }
