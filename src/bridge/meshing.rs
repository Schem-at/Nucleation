//! Meshing: resource packs, mesh configs/results, atlases, item models, and the
//! polling `MeshJob`. Port of `ffi/meshing.rs`.
//!
//! Porting notes (see `PORTING.md`):
//! - All binary outputs (GLB, NUCM, pixels, ZIPs, raw vertex streams) cross the
//!   boundary as base64 strings (`_b64` methods). Float/int streams are the raw
//!   little-endian bytes of the `f32`/`u32` array, base64-encoded.
//! - `schematic_mesh_chunks_with_atlas_progress` (C callback) is replaced by the
//!   polling `MeshJob` API from `stencil/docs/mesh-progress-api.md`:
//!   `MeshJob::start` spawns a background thread, `poll_progress` reads a shared
//!   progress slot without blocking, `take_result` joins and consumes the job.
//! - String lists (`StringArray` in the old ABI) are written as JSON array
//!   strings (`_json` methods).
//! - `chunkmeshresult_chunk_coordinates` (flat int array) becomes
//!   `chunk_count` + `chunk_coordinate_at(index)` returning a `BlockPos`.
//! - `itemmodel_build_resource_pack` (array-of-pointers input) becomes the
//!   `ItemModelPackBuilder` accumulator plus `ItemModelResult::add_to_pack`
//!   (consuming, per PORTING rule 11).
//! - `meshconfig_set_biome(null)` (clearing) becomes `MeshConfig::clear_biome`.
//!
//! Omitted from port: `resourcepack_free`, `meshconfig_free`, `meshresult_free`,
//! `multimeshresult_free`, `chunkmeshresult_free`, `rawmeshexport_free`,
//! `textureatlas_free`, `itemmodel_config_free`, `itemmodel_result_free`,
//! `itemmodel_zip_data_free` — destructors are generated by Diplomat.

use diplomat_runtime::DiplomatWrite;

/// Shared progress slot written by the mesh worker thread and read by
/// `MeshJob::poll_progress`. Phase: 0 = BuildingAtlas, 1 = MeshingChunks,
/// 2 = Complete, 3 = Failed.
pub(crate) struct MeshJobState {
    pub(crate) phase: u8,
    pub(crate) current: u32,
    pub(crate) total: u32,
}

/// Write `bytes` into `out` as standard base64.
pub(crate) fn write_b64(bytes: &[u8], out: &mut DiplomatWrite) {
    use base64::Engine as _;
    use std::fmt::Write as _;
    let _ = write!(
        out,
        "{}",
        base64::engine::general_purpose::STANDARD.encode(bytes)
    );
}

/// Write a `Vec<String>` into `out` as a JSON array string.
pub(crate) fn write_string_list_json(names: &[String], out: &mut DiplomatWrite) {
    use std::fmt::Write as _;
    let json = serde_json::to_string(names).unwrap_or_else(|_| "[]".to_string());
    let _ = write!(out, "{}", json);
}

/// Little-endian byte view of an `f32` slice.
pub(crate) fn f32s_to_le_bytes(floats: &[f32]) -> Vec<u8> {
    let mut bytes = Vec::with_capacity(floats.len() * 4);
    for f in floats {
        bytes.extend_from_slice(&f.to_le_bytes());
    }
    bytes
}

